    private void CadButtonActionPerformed(java.awt.event.ActionEvent evt) {                                          
        if(NomeCadTxt.getText().trim().equals("") || CPFCadTxt.getText().trim().equals("") ||
        		RGCadTxt.getText().trim().equals("")) {
        	JOptionPane.showMessageDialog(null, "Erro ao cadastrar!");
        }else{
        	Pessoa p = new Pessoa();
        	p.setNome(NomeCadTxt.getText());
        	p.setRG(RGCadTxt.getText());
        	p.setCPF(CPFCadTxt.getText());
        	if(Pessoa.validarPessoa(p)) {
        		if(p.salvarPessoa()) {
            		iniciarTablePessoa();
                	limparCamposPessoa();
                	JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");
            	}else JOptionPane.showMessageDialog(null, "Erro ao cadastrar!");
        	}else JOptionPane.showMessageDialog(null, "Erro ao cadastrar!");
        }
        
    }                                         

    private void AtualizaButtonActionPerformed(java.awt.event.ActionEvent evt) {                                               
        if(IdAtuTxt.getText().trim().equals("") || NomeAtuTxt.getText().trim().equals("") || 
        		CPFAtuTxt.getText().trim().equals("") || RGAtuTxt.getText().trim().equals("")) {
        	JOptionPane.showMessageDialog(null, "Erro ao atualizar!");
        }else {
        	Pessoa p = new Pessoa();
        	if((p = p.buscarPessoa(Integer.parseInt(IdAtuTxt.getText()))) != null) {
        		p.setCPF(CPFAtuTxt.getText());
        		p.setNome(NomeAtuTxt.getText());
        		p.setRG(RGAtuTxt.getText());
        		if(Pessoa.validarPessoa(p)) {
        			if(p.atualizarPessoa()) {
            			iniciarTablePessoa();
            			JOptionPane.showMessageDialog(null, "Atualizado com sucesso!");
            			limparCamposPessoa();
            		}else JOptionPane.showMessageDialog(null, "Erro ao atualizar!");
        		}else JOptionPane.showMessageDialog(null, "Erro ao atualizar!");
        	}else JOptionPane.showMessageDialog(null, "Erro ao atualizar!");
        }
    }                                              

    private void RemoverButtonActionPerformed(java.awt.event.ActionEvent evt) {                                              
       if(IdRemTxt.getText().trim().equals("")) {
    	   JOptionPane.showMessageDialog(null, "Erro ao remover!");
       }else {
    	   Pessoa p = new Pessoa();
    	   if((p = p.buscarPessoa(Integer.parseInt(IdRemTxt.getText()))) != null) {
    		  if(Conta.validarConta(p)) {
    			  if(Conta.removerContaAll(p)) {
    				  if(p.removerPessoa(p)) {
    	       			   limparCamposPessoa();
    	       			   iniciarTablePessoa();
    	       			   iniciarTableConta();
    	           		   JOptionPane.showMessageDialog(null, "Removido com sucesso!");
    	       		  }else JOptionPane.showMessageDialog(null, "Erro ao remover!");
    			  }else JOptionPane.showMessageDialog(null, "Erro ao remover!");  
    		  }else {
    			if(p.removerPessoa(p)) {
       			   limparCamposPessoa();
       			   iniciarTablePessoa();
           		   JOptionPane.showMessageDialog(null, "Removido com sucesso!");
       		   	}else JOptionPane.showMessageDialog(null, "Erro ao remover!");
    		  }
    	   }else JOptionPane.showMessageDialog(null, "Erro ao remover!");
       }
    }                                             

