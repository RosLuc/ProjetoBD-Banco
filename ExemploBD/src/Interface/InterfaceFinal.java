    private void iniciarTablePessoa() {
    	DefaultTableModel modelo = new DefaultTableModel() {
    		@Override
    		public boolean isCellEditable(int row, int column) {
    			if(column==1 && column==2 && column==3 && column==4) {
    				return true;
    			}else {
    				return false;
    			}
    		}
    	};
    	jTable1.setModel(modelo);
    	modelo.addColumn("ID");
    	modelo.addColumn("Nome");
    	modelo.addColumn("CPF");
    	modelo.addColumn("RG");
    	List<Pessoa> lista;
        lista = Pessoa.ListPessoa();

        for(int i = 0; i < lista.size(); i++) {
        	Pessoa p = lista.get(i);
        	modelo.addRow(new Object[] {p.getId_pessoa(), p.getNome(), p.getCPF(), p.getRG()});
        }
    }
    
    private void iniciarTableConta() {
    	DefaultTableModel modelo = new DefaultTableModel() {
    		@Override
    		public boolean isCellEditable(int row, int column) {
    			if(column==1 && column==2 && column==3) {
    				return true;
    			}else {
    				return false;
    			}
    		}
    	};
    	jTable2.setModel(modelo);
    	modelo.addColumn("ID");
    	modelo.addColumn("Dono");
    	modelo.addColumn("Saldo");
    	List<Conta> lista;
        lista = Conta.ListConta();

        for(int i = 0; i < lista.size(); i++) {
        	Conta c = lista.get(i);
        	modelo.addRow(new Object[] {c.getId_conta(), c.getPessoa().getNome(), c.getSaldo()});
        }
    }

